package domain;

public class VoteChannelInformation {
    String voteChannelName;
    String channel;
    String url;

    public String getVoteChannelName() {
        return voteChannelName;
    }

    public void setVoteChannelName(String voteChannelName) {
        this.voteChannelName = voteChannelName;
    }

    public String getChannel() {
        return channel;
    }

    public void setChannel(String channel) {
        this.channel = channel;
    }

    public String getUrl() {
        return url;
    }

    public void setUrl(String url) {
        this.url = url;
    }
}
